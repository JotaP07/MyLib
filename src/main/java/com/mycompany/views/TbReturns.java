    private void ConultarTempoReal(){
                try {
            DAOLendings dao = new DAOLendings();
            DefaultTableModel model = (DefaultTableModel) tbItems.getModel();
            model.setRowCount(0);
            List<LendingsM> itemList = dao.listarDevolucao(txtFpesquisar.getText().trim());
            for (LendingsM item : itemList) {
                model.addRow(new Object[]{
                    item.getId(),
                    item.getNomeFuncionario(),
                    item.getNomeCliente(),
                    item.getNomeItem(),
                    item.getDevolvido(),
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

