    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        if (tbClientes.getSelectedRow() > -1) {
            try {
                int userId = (int) tbClientes.getValueAt(tbClientes.getSelectedRow(), 0);
                DAOUsers dao = new DAOUsers();
                UsersM user = dao.getUserById(userId);
                ShowJPanel(new UpUsers(user));
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione um cliente para editar.\n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        DefaultTableModel model = (DefaultTableModel) tbClientes.getModel();
        if (tbClientes.getSelectedRows().length < 1) {
            javax.swing.JOptionPane.showMessageDialog(this, "VocÃª deve selecionar um ou mais usuÃ¡rios para excluir.", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
        } else {
            DAOUsers dao = new DAOUsers();
            for (int i : tbClientes.getSelectedRows()) {
                try {
                    int userId = (int) tbClientes.getValueAt(i, 0); // obtem o ID do usuario pela linha selecionada
                    UsersM user = new UsersM();
                    user.setId(userId);
                    dao.excluir(user); // chama o mÃ©todo excluir passando o objeto UsersM com o ID
                    model.removeRow(i); // remove a linha da tabela
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

