    private void ConsultarTempoReal() {

        try {
            DAOUsers dao = new DAOUsers();
            DefaultTableModel model = (DefaultTableModel) tbClientes.getModel();
            model.setRowCount(0); // Limpar a tabela antes de adicionar novos dados
            List<UsersM> userList = dao.listar(txtFclientes.getText().trim());
            for (UsersM user : userList) {
                model.addRow(new Object[]{
                    user.getId(),
                    user.getNome(),
                    user.getCpf(),
                    user.getSexoNome(),
                    user.getEndereco(),
                    user.getCep(),
                    user.getTelefone(),
                    user.getEmail()
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

