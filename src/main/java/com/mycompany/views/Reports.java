    private void LoadLogs() {
        try {
            DAOReports dao = new DAOReports();
            DefaultTableModel model = (DefaultTableModel) tbRelatorio.getModel();
            model.setRowCount(0);
            List<LogM> itemList = dao.listar("");
            for (LogM item : itemList) {
                model.addRow(new Object[]{
                    item.getAcao(),
                    item.getTimestamp()
                });
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    
