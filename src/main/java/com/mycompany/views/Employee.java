    private void ConsultarTempoReal() {

        try {
            DAOEmployee dao = new DAOEmployee();
            DefaultTableModel model = (DefaultTableModel) tbFuncionarios.getModel();
            model.setRowCount(0); // Limpa a tabela antes de adicionar novos dados
            List<EmployeeM> userList = dao.listar(txtFuncionarios.getText().trim());
            for (EmployeeM func : userList) {
                model.addRow(new Object[]{
                    func.getId(),
                    func.getNomeUsuario(),
                    maskPassword(func.getSenha()),
                    func.getNome(),
                    func.getEmail(),
                    func.getSexoNome(),
                    func.getPapel()
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

