    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        if (tbFuncionarios.getSelectedRow() > -1) {
            try {
                int employeeId = (int) tbFuncionarios.getValueAt(tbFuncionarios.getSelectedRow(), 0);
                DAOEmployee dao = new DAOEmployee();
                EmployeeM employee = dao.getEmployeeById(employeeId);
                ShowJPanel(new UpEmployee(employee));
                
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione um funcionÃ¡rio para editar.\n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        DefaultTableModel model = (DefaultTableModel) tbFuncionarios.getModel();
        if (tbFuncionarios.getSelectedRows().length < 1) {
            javax.swing.JOptionPane.showMessageDialog(this, "VocÃª deve selecionar um ou mais funcionÃ¡rios para excluir.", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
        } else {
            DAOEmployee dao = new DAOEmployee();
            for (int i : tbFuncionarios.getSelectedRows()) {
                try {
                    int funcId = (int) tbFuncionarios.getValueAt(i, 0); // obtem o ID do usuario pela linha selecionada
                    EmployeeM func = new EmployeeM();
                    func.setId(funcId);
                    dao.excluir(func); // chama o mÃ©todo excluir passando o objeto UsersM com o ID
                    model.removeRow(i); // remove a linha da tabela
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

